package pojo;

public class BeneficiaryModifiable {
    private int numeroCuenta, valorDocumentoIdentidadBeneficiario, parentezcoId, Porcentaje;

    public int getNumeroCuenta() {
        return numeroCuenta;
    }

    public void setNumeroCuenta(int numeroCuenta) {
        this.numeroCuenta = numeroCuenta;
    }

    public int getValorDocumentoIdentidadBeneficiario() {
        return valorDocumentoIdentidadBeneficiario;
    }

    public void setValorDocumentoIdentidadBeneficiario(int valorDocumentoIdentidadBeneficiario) {
        this.valorDocumentoIdentidadBeneficiario = valorDocumentoIdentidadBeneficiario;
    }

    public int getParentezcoId() {
        return parentezcoId;
    }

    public void setParentezcoId(int parentezcoId) {
        this.parentezcoId = parentezcoId;
    }

    public int getPorcentaje() {
        return Porcentaje;
    }

    public void setPorcentaje(int porcentaje) {
        Porcentaje = porcentaje;
    }
}
